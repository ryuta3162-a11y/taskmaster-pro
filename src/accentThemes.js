export const ACCENT_STORAGE_KEY = 'taskmaster_accent_id';

const KEYS = ['50', '100', '200', '300', '400', '500', '600', '700', '900'];

export function applyAccentTheme(themeId) {
  const theme = ACCENT_THEMES.find((t) => t.id === themeId) || ACCENT_THEMES[0];
  const root = document.documentElement;
  KEYS.forEach((k) => {
    root.style.setProperty(`--acc-${k}`, theme[k]);
  });
  try {
    localStorage.setItem(ACCENT_STORAGE_KEY, theme.id);
  } catch {
    /* ignore */
  }
}

export function readStoredAccentId() {
  try {
    const v = localStorage.getItem(ACCENT_STORAGE_KEY);
    if (v && ACCENT_THEMES.some((t) => t.id === v)) return v;
  } catch {
    /* ignore */
  }
  return 'indigo';
}
